    private void XuatFileExcel(JTable table, File file){
            Writer out = null;
        try {
            out = new FileWriter(file);
            //Xuat tieu de bang ra file excel
            for(int i=0; i<table.getColumnCount(); i++){
                out.write(table.getColumnName(i) +"\t");
            }
            out.write("\n");
            //Xuat noi dung ra file excel
            for(int i=0; i<8; i++){
                for(int j =0;j<table.getColumnCount();j++){
                    out.write(table.getValueAt(i, j).toString() +"\t");
                }
                out.write("\n");
            }
            out.close();
            System.out.println("Ghi ra file: "+file);
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(StudentJPanel.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } finally {
            try {
                out.close();
            } catch (IOException ex) {
                java.util.logging.Logger.getLogger(StudentJPanel.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
    }
