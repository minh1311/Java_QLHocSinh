    private void tblStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentMouseClicked
       int row = tblStudent.getSelectedRow();
        if (row >= 0) {
            Student student = studentList.get(row);
            String ma = tblStudent.getValueAt(row, 1).toString();
            String ten = tblStudent.getValueAt(row,2).toString();
            String nams = tblStudent.getValueAt(row, 3).toString();
            String tuoi = tblStudent.getValueAt(row, 4).toString();
            String que = tblStudent.getValueAt(row, 5).toString();
            String malop = tblStudent.getValueAt(row, 6).toString();
            txtid.setText(ma);
            txtName.setText(ten) ;
            txtYear.setText(nams);
            txtAge.setText(tuoi);
            txtHometown.setText(que);
            int i=0;
            while(true){
                String clid = jcbMalop.getItemAt(i).toString();
                if(clid.equalsIgnoreCase(malop)){
                    jcbMalop.setSelectedIndex(i);
                    break;
                }
                i++;
                
            }
        }
    }//GEN-LAST:event_tblStudentMouseClicked

