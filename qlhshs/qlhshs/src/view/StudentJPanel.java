    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedIndex = tblStudent.getSelectedRow();
        if (selectedIndex >= 0) {
            Student std = studentList.get(selectedIndex);

            int option = JOptionPane.showConfirmDialog(this, "Ban co muon xoa ban ghi nay khong?");
            System.out.println("option: " + option);

            if (option == 0) {
                StudentModify.delete(std.getStudent_id());

                showStudent();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        String input = JOptionPane.showInputDialog(this, "Nhap ten can tim");
        if (input != null && input.length() > 0) {
            studentList = StudentModify.findByName(input);
            tableModel.setRowCount(0);

            studentList.forEach(student -> {
                tableModel.addRow(new Object[]{tableModel.getRowCount() + 1,student.getStudent_id(), student.getStudent_name(),
                    student.getYear_birth(), student.getAge(), student.getHometown(), student.getClass_id()});
            });
        }else {
            showStudent();
        }
    }//GEN-LAST:event_btnFindActionPerformed

    

