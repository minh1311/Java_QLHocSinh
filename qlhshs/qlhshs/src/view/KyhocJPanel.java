    private void tblkyhocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblkyhocMouseClicked
        int row = tblkyhoc.getSelectedRow();
        if (row >= 0) {
            kyhoc kh = kyhocList.get(row);
            
            String ma = tblkyhoc.getValueAt(row,1).toString();
            String ten = tblkyhoc.getValueAt(row,2).toString();
            txtmaky.setText(ma);
            txttenky.setText(ten);
            jdcstart.setDate(kh.getStart_day());
            jdcend.setDate(kh.getEnd_day());
        }
    }//GEN-LAST:event_tblkyhocMouseClicked

