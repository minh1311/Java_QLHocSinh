    private void btnfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfindActionPerformed
        String input = JOptionPane.showInputDialog(this, "Nhap ten ky can tim");
        if (input != null && input.length() > 0) {
            kyhocList = KyhocModify.findByName(input);
            tableModel.setRowCount(0);

            kyhocList.forEach(kh -> {
                tableModel.addRow(new Object[]{tableModel.getRowCount() + 1, kh.getKy_id(),
                    kh.getTenkyhoc(), kh.getStart_day(), kh.getEnd_day()});
            });
        }else {
            showkyhoc();
        }  
    }//GEN-LAST:event_btnfindActionPerformed
    

